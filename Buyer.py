# A woman went to the market and bought 10 tubers of yam at
# 1000 naira each. 
# She bought 10 crates of eggs at 300 naira per crate.
# She also bought 10 loaves of bread at 500 naira each.
# if the woman took 50000 naira to the market
# Question, using python code, how much does the woman have
# left as a balance.
# solution
Yam = 10 * 1000
Egg = 10 * 300
Bread = 10 * 500
Credit = 50000
Balance = Credit - (Yam+Egg+Bread)
print(Balance)


