# You have the following set of numbers, 22,42,82
# Write a python code that multiply all the numbers and immediately check if the total is equal to 144 if yes 
#print hurray!, it is equal to 144
#Else print oh no!, it is not equal to 144

#Solution:
x = 22
y = 42
z = 82
t = x*y*z
if t == 144:
    print("Hurray!")
else:
    print("oh no! it is not equal to 144")
